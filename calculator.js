const calculator = {
    add: function (a, b) {
        console.log(a + b);
    },
    sub: function (a, b) {
        console.log(a - b);
    },
    mul: function (a, b) {
        console.log(a * b);
    },
    div: function (a, b) {
        console.log(a / b);
    },
    powerOf: function (a, b) {
        console.log(a ** b);
    },
};

calculator.add(6, 2);
calculator.sub(6, 2);
calculator.mul(6, 2);
calculator.div(6, 2);
calculator.powerOf(6, 2);
